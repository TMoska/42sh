#include "libft.h"

void	ft_str2del(char ***tab)
{
	int i;

	i = 0;
	if (!(*tab))
		return ;
	while ((*tab)[i])
	{
		ft_strdel(&((*tab)[i]));
		i++;
	}
	free(*tab);
}
