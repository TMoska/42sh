#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	ln;

	ln = ft_strlen(s) + 1;
	while (--ln >= 0)
		if (s[ln] == (char)c)
			return ((char *)&s[ln]);
	return (0);
}
